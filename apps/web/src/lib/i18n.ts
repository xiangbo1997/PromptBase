export type TranslationKey = NestedPaths<(typeof dictionaries)[Locale]>;

type NestedPaths<T> = T extends Primitive
  ? never
  : {
      [K in keyof T & string]:
        T[K] extends Primitive ? K : K | `${K}.${NestedPaths<Exclude<T[K], Primitive>>}`;
    }[keyof T & string];

function isLocale(value: string): value is Locale {
  return SUPPORTED_LOCALES.includes(value as Locale);
}

export function resolveLocale(value: string | null | undefined): Locale {
  if (value && isLocale(value)) return value;
  return DEFAULT_LOCALE;
}

export function getDictionary(locale: Locale) {
  return dictionaries[locale];
}

export function translate(
  locale: Locale,
  key: TranslationKey,
  params?: Record<string, string | number>,
): string {
  const segments = key.split(".");
  let current: Primitive | DictionaryNode = dictionaries[locale];

  for (const segment of segments) {
    if (!current || typeof current === "string" || typeof current === "number" || typeof current === "boolean") {
      return key;
    }
    current = current[segment];
  }

  if (typeof current !== "string") return key;

  if (!params) return current;

  return current.replace(/\{\{(\w+)\}\}/g, (_, name: string) => {
    const value = params[name];
    return value === undefined ? `{{${name}}}` : String(value);
  });
}
